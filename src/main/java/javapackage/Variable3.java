package javapackage;

public class Variable3 {
	
public static void main(String[] args) {
	
	int[] a= {10, 2, 3, 4, 5}; // Array is non primitive data type , and its index start from zero
	
	System.out.println(a[0]);//10
	System.out.println(a[1]);//2
	System.out.println(a[2]);//3
	System.out.println(a[3]);//4
	System.out.println(a[4]);//5  under a will put index value of each variable value
	System.out.println(a[5]); //error: java.lang.ArrayIndexOutOfBoundsException
}
	
	
	
	
}
